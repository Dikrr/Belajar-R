#VECTOR
print("VECTOR")
angka <- c(1, 2, 3, 4, 5)
print(angka)

web <- c("HTML", "CSS", "Javascript", "PHP", "SQL")
print(web)

angka <- c(1:5)
print(angka)

#mengambil data dari Vector
print(angka[3])
print(angka[[3]])
print(angka[2:4])

#Named Vector
print("Named Vector")
nilai <- c(dikri = 89, budi = 79, dinda = 88)
print(nilai)

#mengambil data dari named vector
print("mengambil data dari named vector")
print(nilai["dikri"])
print(nilai[2])
print(nilai[1:3])